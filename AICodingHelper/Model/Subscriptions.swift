    case lowWeekly = "LOW_WEEKLY"
    case lowMonthly = "LOW_MONTHLY"
    case mediumWeekly = "MEDIUM_WEEKLY"
    case mediumMonthly = "MEDIUM_MONTHLY"
    case highWeekly = "HIGH_WEEKLY"
    case highMonthly = "HIGH_MONTHLY"
    
}
